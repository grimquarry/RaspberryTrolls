void Level::Draw(Window& l_window)
{
  int tileIndex = 0;
  for(int y = 0; y < 14; y++)
  {
    for(int x = 0; x < 64; x++)
    {
      if(sLevelMap[tileIndex] == '-')
      {
        sprite.setPosition((x * 200), y * 75);
        //std::cout << "\nX: " << x*200 << "\nY: " << y * 75 << std::endl;
        //std::cout << tileIndex << std::endl;
        l_window.Draw(sprite);
      }
      tileIndex++;
    }
  }

}
